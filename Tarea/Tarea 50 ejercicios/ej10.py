# Ejercicio 10: Calcular el precio de venta de un artículo para obtener una ganancia del 30%
precio_compra = 100  # Precio de compra del artículo
ganancia_porcentaje = 30  # Porcentaje de ganancia deseado

# Calculamos el precio de venta para obtener la ganancia deseada
precio_venta = precio_compra * (1 + ganancia_porcentaje / 100)

# Imprimimos el precio de venta
print(f"El precio de venta del artículo para obtener una ganancia del 30% es: {precio_venta:.2f}")
