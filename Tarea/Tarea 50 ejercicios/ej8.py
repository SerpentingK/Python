# Ejercicio 8: Calcular el nuevo salario de un obrero con un incremento del 25%
salario_anterior = 20000  # Salario anterior del obrero
incremento_porcentaje = 25  # Incremento del 25%

# Calculamos el nuevo salario con el incremento
nuevo_salario = salario_anterior * (1 + incremento_porcentaje / 100)

# Imprimimos el nuevo salario
print(f"El nuevo salario del obrero es: {nuevo_salario:.2f}")
