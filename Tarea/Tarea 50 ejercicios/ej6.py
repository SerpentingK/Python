# Ejercicio 6: Obtener la equivalencia en dólares de una cantidad en pesos
tipo_cambio = 0.05  # Tipo de cambio actual (1 peso = 0.05 dólares)
cantidad_pesos = 1000  # Cantidad en pesos mexicanos

# Calculamos la equivalencia en dólares
equivalencia_dolares = cantidad_pesos * tipo_cambio

# Imprimimos la equivalencia en dólares
print(f"La equivalencia en dólares de {cantidad_pesos} pesos es: {equivalencia_dolares} dólares.")
