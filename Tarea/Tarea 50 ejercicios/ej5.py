# Ejercicio 5: Calcular la edad de una persona
anio_actual = 2024  # Año actual
anio_nacimiento = 1990  # Año de nacimiento

# Calculamos la edad restando el año de nacimiento al año actual
edad = anio_actual - anio_nacimiento

# Imprimimos la edad
print(f"La persona tiene {edad} años de edad.")
