# Ejercicio 9: Calcular la cantidad de dinero que recibirá cada área de un hospital
presupuesto_total = 1000000  # Presupuesto total del hospital

# Calculamos la cantidad de dinero que recibirá cada área
cirugia = presupuesto_total * 0.40
traumatologia = presupuesto_total * 0.30
pediatria = presupuesto_total * 0.30

# Imprimimos los resultados
print(f"Dinero para Cirugía: {cirugia}")
print(f"Dinero para Traumatología: {traumatologia}")
print(f"Dinero para Pediatría: {pediatria}")
