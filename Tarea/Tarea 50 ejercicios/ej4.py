# Ejercicio 4: Porcentaje de hombres y mujeres en un grupo de estudiantes
num_hombres = 25  # Número de hombres en el grupo
num_mujeres = 35  # Número de mujeres en el grupo
total_estudiantes = num_hombres + num_mujeres  # Total de estudiantes en el grupo

# Calculamos el porcentaje de hombres y mujeres
porcentaje_hombres = (num_hombres / total_estudiantes) * 100
porcentaje_mujeres = (num_mujeres / total_estudiantes) * 100

# Imprimimos los resultados
print(f"Porcentaje de hombres: {porcentaje_hombres:.2f}%")
print(f"Porcentaje de mujeres: {porcentaje_mujeres:.2f}%")
